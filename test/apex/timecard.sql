-- create tables
create table employees (
    id                             number generated by default on null as identity 
                                   constraint employees_id_pk primary key,
    name                           varchar2(255 char)
                                   constraint employees_name_unq unique not null,
    email                          varchar2(255 char)
                                   constraint employees_email_unq unique not null,
    country                        varchar2(6 char) not null,
    employee_type                  varchar2(9 char) constraint employees_employee_type_ck -- bug: type_vc3_ck
                                   check (employee_type/*bug: _vc30_/n*/ in ('FULL TIME','PART TIME')),
    approver                       varchar2(3 char) not null,
    row_version                    integer not null
)
;

-- table index
create index employees_i1 on employees (approver);

create table timecards (
    id                             number generated by default on null as identity 
                                   constraint timecards_id_pk primary key,
    employee_id                    number
                                   constraint timecards_employee_id_fk
                                   references employees ,
    status                         varchar2(9 char) default on null 'SUBMITTED' constraint timecards_status_ck
                                   check (status in ('SUBMITTED','APPROVED','DECLINED')),
    week_of                        date not null,
    sunday                         integer default on null '0'
                                   constraint timecards_sunday_bet
                                   check (sunday between 0 and 24),
    monday                         integer default on null '0'
                                   constraint timecards_monday_bet
                                   check (monday between 0 and 24),
    tuesday                        integer default on null '0'
                                   constraint timecards_tuesday_bet
                                   check (tuesday between 0 and 24),
    wednesday                      integer default on null '0'
                                   constraint timecards_wednesday_bet
                                   check (wednesday between 0 and 24),
    thursday                       integer default on null '0'
                                   constraint timecards_thursday_bet
                                   check (thursday between 0 and 24),
    friday                         integer default on null '0'
                                   constraint timecards_friday_bet
                                   check (friday between 0 and 24),
    saturday                       integer default on null '0'
                                   constraint timecards_saturday_bet
                                   check (saturday between 0 and 24),
    comments                       varchar2(4000 char),
    confirmation_code_v50          varchar2(4 char),
    row_version                    integer not null,
    created                        date not null,
    created_by                     varchar2(255 char) not null,
    updated                        date not null,
    updated_by                     varchar2(255 char) not null
)
;

-- table index
create index timecards_i1 on timecards (employee_id);


-- triggers
create or replace trigger employees_biu
    before insert or update 
    on employees
    for each row
begin
    :new.email := lower(:new.email);   --<-- reordered
    if inserting then
        :new.row_version := 1;
    elsif updating then
        :new.row_version := nvl(:old.row_version,0) + 1;
    end if;
end employees_biu;
/

create or replace trigger timecards_biu
    before insert or update 
    on timecards
    for each row
begin
    if inserting then
        :new.row_version := 1;
    elsif updating then
        :new.row_version := nvl(:old.row_version,0) + 1;
    end if;
    if inserting then
        :new.created := sysdate;
        :new.created_by := coalesce(sys_context('APEX$SESSION','APP_USER'),user);
    end if;
    :new.updated := sysdate;
    :new.updated_by := coalesce(sys_context('APEX$SESSION','APP_USER'),user);
end timecards_biu;
/


