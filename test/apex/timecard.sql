create or replace package  body employees_api
is
 
    procedure get_row (
        p_id                           in  number,
        p_name                         out varchar2,
        p_email                        out varchar2,
        p_country                      out varchar2,
        p_employee_type                out varchar2,
        p_approver                     out varchar2
    )
    is
    begin
        for c1 in (select * from employees where id = p_id) loop
            p_name := c1.name;
            p_email := c1.email;
            p_country := c1.country;
            p_employee_type := c1.employee_type;
            p_approver := c1.approver;
        end loop;
    end get_row;

 
    procedure insert_row  (
        p_id                           in number default null,
        p_name                         in varchar2 default null,
        p_email                        in varchar2 default null,
        p_country                      in varchar2 default null,
        p_employee_type                in varchar2 default null,
        p_approver                     in varchar2 default null
    )
    is
    begin
        insert into employees (
            id,
            name,
            email,
            country,
            employee_type,
            approver
        ) values (
            p_id,
            p_name,
            p_email,
            p_country,
            p_employee_type,
            p_approver
        );
    end insert_row;

    procedure update_row  (
        p_id                           in number default null,
        p_name                         in varchar2 default null,
        p_email                        in varchar2 default null,
        p_country                      in varchar2 default null,
        p_employee_type                in varchar2 default null,
        p_approver                     in varchar2 default null
    )
    is
    begin
        update  employees set 
            id = p_id,
            name = p_name,
            email = p_email,
            country = p_country,
            employee_type = p_employee_type,
            approver = p_approver
        where id = p_id;
    end update_row;

    procedure delete_row (
        p_id                           in number
    )
    is
    begin
        delete from employees where id = p_id;
    end delete_row;

end employees_api;
/


