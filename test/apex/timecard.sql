create or replace view employee_timecards as 
select 
    employees.id                                       employee_id,
    employees.name                                     name,
    employees.email                                    email,
    employees.country                                  country,
    employees.employee_type                            employee_type,
    employees.approver                                 approver,
     employees.row_version                              employee_row_version,
   timecards.id                                       timecard_id,
    timecards.status                                   status,
    timecards.week_of                                  week_of,
    timecards.sunday                                   sunday,
    timecards.monday                                   monday,
    timecards.tuesday                                  tuesday,
    timecards.wednesday                                wednesday,
    timecards.thursday                                 thursday,
    timecards.friday                                   friday,
    timecards.saturday                                 saturday,
    timecards.comments                                 comments,
    timecards.confirmation_code_v50                    confirmation_code_v50,
     timecards.row_version                              timecard_row_version,
   timecards.created                                  timecard_created,
    timecards.created_by                               timecard_created_by,
    timecards.updated                                  timecard_updated,
    timecards.updated_by                               timecard_updated_by
from 
    employees,
    timecards
where
    timecards.employee_id(+) = employees.id
